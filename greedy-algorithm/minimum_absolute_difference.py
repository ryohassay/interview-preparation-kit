def minimumAbsoluteDifference(arr):
    # Write your code here
    size = len(arr)
    arr.sort()
    min = arr[1] - arr[0]
    for i in range(1, size - 1):
        diff = arr[i+1] - arr[i]
        if diff < min:
            min = diff
    return min


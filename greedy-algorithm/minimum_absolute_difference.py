def minimumAbsoluteDifference(arr):
    # Write your code here
    size = len(arr)
    min = abs(arr[0] - arr[1])
    for i in range(size - 1):
        for j in range(i + 1, size):
            diff = abs(arr[i] - arr[j])
            if diff < min:
                min = diff
    return min

